    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
         // TODO add your handling code here:
        if("".equals(txtID.getText()) || "".equals(txtName.getText()) || "".equals(txtManager.getText()) || "".equals(txtDate.getText()) || (!btnDivision.isSelected() && !btnFaculty.isSelected())){
             JOptionPane.showConfirmDialog(this,"Not enough information !!","Notification", WIDTH);
        }
        else{
        DepartmentDTO newDp = new DepartmentDTO();
        newDp.setID(txtID.getText());
        newDp.setName(txtName.getText());
        if(btnDivision.isSelected()){
            newDp.setType("Division");
        }else{
            newDp.setType("Faculty");
        }
        newDp.setManager(txtManager.getText());
        newDp.setQuantity(newDp.getNumber() + "");
        newDp.setDate(txtDate.getText());
            if(!CheckIDandName(newDp)){
                JOptionPane.showConfirmDialog(this,"Duplicate ID and Name information !!","Notification", WIDTH);
            }
            else if(!isValidDateFormat(newDp.getDate())){
                JOptionPane.showConfirmDialog(this,"Incorrect date format entered !!","Notification", WIDTH);
            }
            else{
                list.add(newDp);
                SaveData();
                loadTable();       
                txtID.setText("");
                txtName.setText("");
                btnGroup.clearSelection();
                txtManager.setText("");
                txtDate.setText("");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed
                
    
    private void btnFixActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFixActionPerformed
        // TODO add your handling code here:
        rows = tblDepartment.getSelectedRow();
        if(rows !=-1){
        DepartmentDTO newDp = new DepartmentDTO();
        newDp.setID(txtID.getText());
        newDp.setName(txtName.getText());
        if(btnDivision.isSelected()){
            newDp.setType("Division");
        }else{
            newDp.setType("Faculty");
        }
        newDp.setManager(txtManager.getText());
        newDp.setQuantity(newDp.getNumber() + "");
        newDp.setDate(txtDate.getText());
            if(!isValidDateFormat(newDp.getDate())){
                JOptionPane.showConfirmDialog(this,"Incorrect date format entered !!","Notification", WIDTH);
            }
            else if(!Check(newDp,rows)){
                JOptionPane.showConfirmDialog(this,"Duplicate ID and Name information !!","Notification", WIDTH);
            }
            else{
                list.set(rows,newDp);
                SaveData();
                loadTable();
                txtID.setText("");
                txtName.setText("");
                txtManager.setText("");
                txtDate.setText("");
            }
        
        }else{
            JOptionPane.showConfirmDialog(this,"No line selected!","Notification", HEIGHT);
        }
        
    }//GEN-LAST:event_btnFixActionPerformed

