    private void btnFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterActionPerformed
        // TODO add your handling code here:]
       ArrayList<DepartmentDTO> data = new ArrayList<>();
       list.clear();
       for(DepartmentDTO newDp:listDp){
           data.add(newDp);
       }
       String nameType = (String)cbDepartment.getSelectedItem();
       if(nameType.equals("All")){           
           list.addAll(data);
       }else{
           for(DepartmentDTO newDp:data){
               if(newDp.getType().equals(nameType)){
                   list.add(newDp);
               }
           }
       }
       loadTable();
    }//GEN-LAST:event_btnFilterActionPerformed

